    public int KTDate(){
        String sql = "SELECT `Date_Borrow` FROM `borrowbook` WHERE `Student_ID` =? AND`Book_ID`=?" ;
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtSID_RB.getText());
            pst.setString(2, txtBID.getText());
            rs = pst.executeQuery();
            if(rs.next()){
            String date_br = rs.getString("Date_Borrow");
            
            
            Date d1 = null;
            Date d2 = null;
            Date currentDate = new Date();
            
                
                String endDate = sdf.format(currentDate);

                d1 = sdf.parse(date_br);
                d2 = sdf.parse(endDate);

                long getDiff = d2.getTime() - d1.getTime();

                long getDaysDiff = getDiff / (24 * 60 * 60 * 1000);
                
                if(getDaysDiff<30){
                    return 1;
                }
                else if(getDaysDiff>30){
                    return 0;
                }
             rs.close();
             pst.close();
            }
       } catch (Exception ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 2;
        
    }
