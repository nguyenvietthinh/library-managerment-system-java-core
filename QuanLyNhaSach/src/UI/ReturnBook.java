    public void LoadBrB(){
        tbm.setNumRows(0);
        int number;
        Vector row,column;
        column = new Vector();
        String sql = "SELECT b.* FROM `borrowbook`a,`book`b  WHERE a.`Book_ID` = b.`Book_ID` AND a.`Student_ID`=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtSID_RB.getText());
            rs = pst.executeQuery();
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();//Trả về số cột
            
            for(int i= 1;i <= number;i++){
                column.add(metadata.getColumnName(i));//Lấy ra tiêu đề cho các cột
            }
            tbm.setColumnIdentifiers(column);
            while(rs.next()){
               
                row = new Vector();
                for(int i = 1; i<=number;i++){
                    row.addElement(rs.getString(i));
                }
                tbm.addRow(row);
                jTable2.setModel(tbm);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
