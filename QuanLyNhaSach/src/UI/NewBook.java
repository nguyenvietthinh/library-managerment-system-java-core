    private int KTMaS(){
        String ma = txtBID.getText();
        int row = tbm.getRowCount();
        for(int i=0;i<row;i++){
            if(tbm.getValueAt(i, 0).toString().equalsIgnoreCase(ma)){
                return 1;
            }
        }
       return 0;
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
       if(KTMaS()==1){
        JOptionPane.showMessageDialog(null, "Đã tồn tại mã sách "+txtBID.getText() );
       }else{
            
            String sql = "INSERT INTO `book`(`Book_ID`, `Name`, `Edition`, `Author`, `Price`, `Pages`) VALUES (?,?,?,?,?,?)";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1,txtBID.getText());
            pst.setString(2,txtBName.getText());
            pst.setString(3, (String) cbEdB.getSelectedItem());
            pst.setString(4,txtPubB.getText());
            pst.setString(5,txtPrB.getText());
            pst.setString(6,txtPgB.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Sách mới đã được thêm");
            LoadDS();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
       }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tableDsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDsMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()==1){
            tableDs = (JTable) evt.getSource();
            int row = tableDs.getSelectedRow();
            int column = tableDs.getSelectedColumn();
            txtBID.setText(tbm.getValueAt(row, 0).toString());
            txtBName.setText(tbm.getValueAt(row, 1).toString());
            cbEdB.setSelectedItem(tbm.getValueAt(row, 2).toString());
            txtPubB.setText(tbm.getValueAt(row, 3).toString());
            txtPrB.setText(tbm.getValueAt(row, 4).toString());
            txtPgB.setText(tbm.getValueAt(row, 5).toString());
            txtBID.setEditable(false);
            
        }
        
    }//GEN-LAST:event_tableDsMouseClicked

