    public void LoadDS(){
        String sql= "SELECT * FROM `book` ";
        tbm.setRowCount(0);
        Vector column, row;
         int number;
        column = new Vector();
        
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            ResultSetMetaData meta = rs.getMetaData();
            number = meta.getColumnCount();//số cột
            for(int i=1;i<=number;i++){
                column.add(meta.getColumnName(i));
            }
            tbm.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++){
                    row.addElement(rs.getString(i));
                }
                tbm.addRow(row);
                tableDs.setModel(tbm);
            }
           
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try {
                pst.close();
                rs.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
            
        }
        
        
    }
